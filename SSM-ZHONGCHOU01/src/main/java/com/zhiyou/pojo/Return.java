package com.zhiyou.pojo;

import java.util.Date;

public class Return {
  private int id;
  private String rule;
  private String chance;
  private String prize;
  private String prizimage;
  private int money;
  private int amount;
  private Date time;
  private int people;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getRule() {
	return rule;
}
public void setRule(String rule) {
	this.rule = rule;
}
public String getChance() {
	return chance;
}
public void setChance(String chance) {
	this.chance = chance;
}
public String getPrize() {
	return prize;
}
public void setPrize(String prize) {
	this.prize = prize;
}
public String getPrizimage() {
	return prizimage;
}
public void setPrizimage(String prizimage) {
	this.prizimage = prizimage;
}
public int getMoney() {
	return money;
}
public void setMoney(int money) {
	this.money = money;
}
public int getAmount() {
	return amount;
}
public void setAmount(int amount) {
	this.amount = amount;
}
public Date getTime() {
	return time;
}
public void setTime(Date time) {
	this.time = time;
}
public int getPeople() {
	return people;
}
public void setPeople(int people) {
	this.people = people;
}
	
}
